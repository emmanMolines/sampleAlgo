public class Calculator {
	
	
	/***
	 * 
	 * @param x constructor for the first number
	 * @param y constructor for the second number
	 * @return added value
	 */
    public static double addition(double x, double y){

        return x+y;

    };
    
    
	/***
	 * 
	 * @param x constructor for the first number
	 * @param y constructor for the second number
	 * @return added value
	 */
	public static double subtract(double x, double y){
	
	      return x-y;
	 };
	
}
