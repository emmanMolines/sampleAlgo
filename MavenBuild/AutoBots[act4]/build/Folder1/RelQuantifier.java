public class RelQuantifier {
	
	
	/***
	 * 
	 * @param x constructor for the first number
	 * @param y constructor for the second number
	 * @return boolean result value
	 */
    public static boolean greaterThanOrEqualTo(double x, double y){

        return x>=y;
    };
    
    
    /***
	 * 
	 * @param x constructor for the first number
	 * @param y constructor for the second number
	 * @return boolean result value
	 */
    public static boolean lesserThan(double x, double y){
        return x<y;
    }

}